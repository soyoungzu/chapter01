package chap04;

public class ChangeValue1 {
    public static void main(String[] args) {
        // 년도
        int year = 2002;

        System.out.print("한국 월드컵은 ");
        System.out.print(year);
        System.out.println("년도에 개최되었습니다.");

        // 변수에 다른 값 할당
        year = 2018;
        System.out.print("동계올림픽이 ");
        System.out.print(year);
        System.out.println("년도에 개최되었습니다.");
    }
}
